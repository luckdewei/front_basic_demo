// & 按位与 AND
//  1 & 1 = 1 1 & 0 = 0

/**
 * 1 : 0001
 * 3 : 0011
 */

console.log(1 & 3) // 1


// | 按位或 OR
//  1 | 1 = 1 1 | 0 = 1

console.log(1 | 3) // 3

// ^ 按位异或 XOR
//  1 ^ 1 = 0 1 ^ 0 = 1 0 ^ 0 = 0

console.log(1 ^ 3) // 2

// ~ 按位非 NOT
// ~1 = 0  ~0 = 1

console.log(~1) // -2
console.log(~0) // -1